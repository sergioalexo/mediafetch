const MESSAGES: Record<Lang, Record<MsgKey, string>> = { en, uk, ru };

export function translate(
  lang: Lang,
  key: MsgKey,
  vars?: Record<string, string | number>
): string {
  let text = MESSAGES[lang]?.[key] ?? en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replaceAll(`{${k}}`, String(v));
    }
  }
  return text;
}

/** Current language from settings ("en" until they load). */
export function useLang(): Lang {
  return useApp((s) => s.settings?.language) ?? "en";
}

/** Translation hook bound to the current language. */
export function useT() {
  const lang = useLang();
  return (key: MsgKey, vars?: Record<string, string | number>) =>
    translate(lang, key, vars);
}

